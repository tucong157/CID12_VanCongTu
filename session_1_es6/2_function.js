function sum(a, b){
return a + b;
}
console.log('sum = ', sum(1, 2));

const sub = function(a, b){
    return a - b;
}
console.log('sub = ', sub(10, 4));


const mutiply = (a, b) => {
    return a * b;
}
console.log('mutiply = ', mutiply(2, 8));

const divide = (a, b) => a / b;
console.log('divide = ', divide(10, 2));

const pow = (a, b) => a**b;
console.log('pow = ', pow(2, 4));
